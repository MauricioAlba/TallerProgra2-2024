    void Llenar(){
        try {
            conn = Mysql.getConnection();
            String[]titulos = {"id_equipo", "identificacion", "tipo", "fecha_adquisicion", "especificaciones","ubicacion"}; 
            String sql = "select * from equipos";
            model = new DefaultTableModel(null, titulos);
            sent= conn.createStatement();
            ResultSet rs=sent.executeQuery(sql);
            
            String[]fila = new String[6];
            while(rs.next()){
                fila[0]=rs.getString("id_equipo");
                fila[1]=rs.getString("identificacion");
                fila[2]=rs.getString("tipo");
                fila[3]=rs.getString("fecha_adquisicion");
                fila[4]=rs.getString("especificaciones");
                fila[5]=rs.getString("ubicacion");
                model.addRow(fila);
            }
            jTable1.setModel(model);
        }
        catch (Exception e) {
            e.printStackTrace();
        }
    }
