    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getButton()==1){
            int fila = jTable1.getSelectedRow();
            try {
                Habilitar();
                String sql = "select * from equipos where id_equipo="+jTable1.getValueAt(fila,0);
                sent = conn.createStatement();
                ResultSet rs = sent.executeQuery(sql);
                rs.next();
                jTextField1.setText(rs.getString("identificacion"));
                jTextField2.setText(rs.getString("tipo"));
                jTextField3.setText(rs.getString("fecha_adquisicion"));
                jTextField4.setText(rs.getString("especificaciones"));
                jTextField5.setText(rs.getString("ubicacion"));

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

